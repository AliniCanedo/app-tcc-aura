        Definimos um produto como algo que pode ser oferecido a um mercado para apreciação, aquisição, uso ou consumo e que pode satisfazer um desejo ou necessidade. Produtos incluem mais do que apenas bens tangíveis. Definimos amplamente, incluem objetos físicos, serviços, eventos, pessoas, lugares, organizações, ideias ou um misto de todas essas entidades. 
        
        Em relação ao produto, é importante destacar que cada um possui um ciclo de vida. O ciclo de vida de um produto pode ser retratado por uma curva que cresce, se mantém e depois entra em declínio. Segundo Kotler e Keller (2012), esse ciclo pode ser dividido em estágios: Introdução (produto é introduzido ao mercado, não há lucros neste estágio); Crescimento (período de rápida aceitação do mercado e melhoria substancial dos lucros); Maturidade (demanda nivela-se e as vendas são para reposição. Fase de aumento da concorrência); e Declínio (produto começa a perder atração para o consumidor e as vendas despencam). 
        
