        2.1.1 Produto 
        
        Produto é tudo aquilo capaz de satisfazer um desejo ou uma necessidade. O mais importante de um produto é o serviço que ele presta, sendo uma síntese do que o vendedor pretende e o consumidor percebe. Dessa forma, o produto pode ser um bem, um serviço ou uma ideia, seguindo a premissa que o produto é algo que possa ser comprado e vendido. 
        
